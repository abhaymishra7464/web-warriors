const TOPIC_ALIASES = {
  Arrays: 'Array',
  'Hash Table': 'Array',
  Strings: 'String',
  'Binary Tree': 'Tree',
  DFS: 'Graph',
  BFS: 'Graph',
  'Depth-First Search': 'Graph',
  'Breadth-First Search': 'Graph',
  Graphs: 'Graph',
  'Heap (Priority Queue)': 'Heap'
};

export const normalizeTopicName = (topic = '') => {
  const clean = String(topic || '').trim();
  if (!clean) return 'Array';
  return TOPIC_ALIASES[clean] || clean;
};

export const getQuestionsForTopic = (topic, level = 'Beginner', limit = 5) => {
  const normalized = normalizeTopicName(topic);
  const items = QUESTION_BANK[normalized] || QUESTION_BANK.Array;

  let order = ['easy', 'medium', 'hard'];
  if (String(level).toLowerCase() === 'intermediate') {
    order = ['medium', 'easy', 'hard'];
  }
  if (String(level).toLowerCase() === 'advanced') {
    order = ['medium', 'hard', 'easy'];
  }

  const ranked = order.flatMap((difficulty) => items.filter((item) => item.difficulty === difficulty));
  return ranked.slice(0, limit);
};

export const getFollowupQuestions = ({ topic, level = 'Beginner', solvedQuestionId = '', mistakeTitle = '', limit = 4 }) => {
  const candidates = getQuestionsForTopic(topic, level, 12).filter((item) => item.id !== String(solvedQuestionId || ''));

  if (/boundary|off[- ]by[- ]one|index/i.test(mistakeTitle)) {
    return [...candidates].sort((a, b) => (a.difficulty === 'easy' ? -1 : 1)).slice(0, limit);
  }
  if (/complexity|optimi/i.test(mistakeTitle)) {
    return [...candidates].sort((a, b) => (a.difficulty === 'medium' ? -1 : 1)).slice(0, limit);
  }
  return candidates.slice(0, limit);
};
